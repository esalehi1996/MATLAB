% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)

x0 = str2double(get(handles.edit3, 'string'));
y0 = str2double(get(handles.edit4, 'string'));
h = str2double(get(handles.edit5, 'string'));
x = str2double(get(handles.edit6, 'string'));
n = str2double(get(handles.edit7, 'string'));
f = get(handles.edit1, 'string');
if h ~= 0
    cnames = {'Xi', 'Yi'};
    set(handles.uitable1, 'ColumnName', cnames);

    switch get(handles.popupmenu1, 'value')
        case 1 %taylor
            history = Taylor(f, x0, y0, h, (x - x0) / h, n);
        case 2 %euler
            history = Euler(f, x0, y0, h, (x - x0) / h);
        case 3 %modeuler
            history = ModifiedEuler(f, x0, y0, h, (x - x0) / h);
        case 4 %midpoint 
            history = RKMidPoint(f, x0, y0, h, (x - x0) / h);
        case 5 %thirdorder
            history = RK3rdOrder(f, x0, y0, h, (x - x0) / h);
        case 6 %fourthorder
            history = RK4thOrder(f, x0, y0, h, (x - x0) / h);
        case 7 %adams-multon
            history = AdamsMulton3rdOrder(f, x0, y0, h, (x - x0) / h);
        case 8
            history = AdamsMulton4thOrder(f, x0, y0, h, (x - x0) / h);
    end
    format short
    set(handles.uitable1, 'Data', history);
end
